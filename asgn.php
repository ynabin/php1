<?php
//simple assign operator
$y=75;
echo $y, "/n";
//Add then assign operator
$y=100;
$y+=200;
echo $y, "\n";

//subtract then assign operator
$y = 70;
$y -=10;
echo $y, "\n";

//Multiply the assign operator
$y = 30;
$y = 20;
echo $y, "\n";

//Divide then assign operator
$y = 100;
$y = 5;
echo $y, "\n";

//Divide then assign(remainder) operator
$y = 50;
$y %=5;
echo $y;
?>